                    <h4>Emerging Infectious diseases</h4>
                    <div class="focus-content">
                        <p>Emerging and reemerging infectious diseases with potential to cause disease outbreaks form
                            majority of public health challenges experienced globally. The 21st century has experienced
                            several infectious disease outbreaks and pandemics such as Ebola virus disease, and now the
                            coronavirus disease. The COVID-19 pandemic has reiterated the need for multidimensional
                            approaches in fighting pandemics, including the integration of social science into emergency
                            risk communication.<br>
                            Uganda's tropical climate, population density, and poor socioeconomic indicators place the
                            country at risk of annual disease outbreaks. In the last several years, Uganda has
                            experienced outbreaks of cholera and other infectious diseases. These outbreaks have
                            highlighted the critical need for risk communication as an integral component of outbreak
                            response.<br />
                            The goal of social and behavior change interventions in pandemic preparedness and response
                            is to reduce morbidity and mortality by preventing transmission among those who are well and
                            those caring for the sick and by preventing further transmission of the disease among those
                            who are sick.
                        </p>

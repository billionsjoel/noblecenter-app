                    <h4>Food security and improved Nutrition</h4>
                    <div class="focus-content">
                        <p>Households that are vulnerable to food insecurity often demonstrate poor nutrition outcomes.
                            Women and children are at greater risk of malnutrition than men: twice as many women suffer
                            from malnutrition as men, and girls are more likely to die from malnutrition than boys
                            (FAO). Nutrition is one of the critical drivers for economic growth and poverty reduction,
                            making it a key component of promoting international stability and security. Although
                            progress has been made over the past two decades in reducing micronutrient deficiency,
                            progress regarding other indicators related to malnutrition and food security (e.g., anemia,
                            wasting, stunting) remains to be seen. To tackle this complex problem sustainably, the
                            approach must be gender-sensitive to utilize social and behavior change strategies to
                            transform community norms.
                        </p>
                    </div>

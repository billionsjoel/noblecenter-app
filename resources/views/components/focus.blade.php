                    <h4>Emergency Preparedness, Response and Recovery</h4>
                    <div class="focus-content">
                        <p>Emergencies are acute events that characteristically take populations by surprise and, most
                            likely, spread fear and anxiety. Examples of emergencies include natural disasters such as
                            earthquakes, floods, droughts etc.
                            During emergencies, a coordinated, clear and credible strategy aids in minimizing resulting
                            health, social and economic costs. However, the uncertainty that characterizes emergencies
                            typically fuels fear and panic among communities, and spreads misconceptions and rumors that
                            affect people’s ability to appropriately respond to the emergency.
                            Our Center designs strategies that are crucial to addressing the behavioral and social
                            aspects
                            of health risks that precede and follow an emergency, and as such need to be integrated into
                            the
                            response agenda from the outset. It is critical to ensure that communication efforts are
                            harmonized, relevant, timely, financially supported and aligned among all of the
                            preparedness
                            technical teams. A strong and united voice heard through various sources can determine
                            whether
                            an emergency spirals out of control or is brought into check as soon as possible.
                        </p>
                    </div>
                </div>
